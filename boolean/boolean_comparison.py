# Boolean Comparison Results

a = 10
b = 20

print(a > b)
print(a < b)
print(a == b)
print(a != b)

# Using variables
x = 50
y = 25

print(x >= y)
print(x <= y)

# Comparing strings
print("apple" == "apple")
print("apple" == "banana")

# Comparison inside if
number = -3

if number > 0:
    print("Positive")
else:
    print("Zero or Negative")
